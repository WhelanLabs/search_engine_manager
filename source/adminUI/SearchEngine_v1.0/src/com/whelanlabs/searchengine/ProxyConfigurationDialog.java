private void proxyConfigOKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proxyConfigOKButtonActionPerformed
  setVisible(false);
  
   Properties properties = new Properties();
   if (proxyEnabledCheckBox.isSelected()) {
      properties.setProperty("proxyEnabled", "true");
   }
   else {
      properties.setProperty("proxyEnabled", "false");
   }
   
   properties.setProperty("proxyHost", hostnameTextField.getText());
   properties.setProperty("proxyPort", portTextField.getText());
   properties.setProperty("proxyUser", usernameTextField.getText());
   properties.setProperty("proxyPassword", passwordField.getText());

   try {
      String filename = appRootDirectory + "/config/proxy.properties";
      properties.store(new FileOutputStream(filename), null);
   } catch (IOException ex) {
      Logger.getLogger(AdminUIView.class.getName()).log(Level.SEVERE, null, ex);
   }
  
   TemplateUtils.updateNutchConfig(appRootDirectory);

}//GEN-LAST:event_proxyConfigOKButtonActionPerformed

