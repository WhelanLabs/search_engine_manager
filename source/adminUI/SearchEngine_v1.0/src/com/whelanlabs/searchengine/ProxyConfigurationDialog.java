   private void setValues() {
      Properties properties = new Properties();
      try {
         String filename = appRootDirectory + "/config/proxy.properties";
         properties.load(new FileInputStream(filename));
      } catch (IOException ex) {
         Logger.getLogger(AdminUIView.class.getName()).log(Level.SEVERE, null, ex);
      }

      hostnameTextField.setText(properties.getProperty("proxyHost"));
      portTextField.setText(properties.getProperty("proxyPort"));
      usernameTextField.setText(properties.getProperty("proxyUser"));
      passwordField.setText(properties.getProperty("proxyPassword"));
      if ("true".equals(properties.getProperty("proxyEnabled"))) {
         proxyEnabledCheckBox.setSelected(true);
      } else {
         proxyEnabledCheckBox.setSelected(false);
      }
      setmodifyableOrNot();
   }
  
   private void setmodifyableOrNot() {
      // if selected, dialogs are modifyable, else they are not
      if (!proxyEnabledCheckBox.isSelected()) {
         hostnameTextField.setEnabled(false);
         passwordField.setEnabled(false);
         portTextField.setEnabled(false);
         usernameTextField.setEnabled(false);
      } else {
         hostnameTextField.setEnabled(true);
         passwordField.setEnabled(true);
         portTextField.setEnabled(true);
         usernameTextField.setEnabled(true);
      }
   }
